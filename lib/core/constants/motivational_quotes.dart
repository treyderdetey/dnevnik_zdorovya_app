  static Map<String, String> getQuoteOfTheDay({bool? isFemale}) {
    final dayOfYear = DateTime.now()
        .difference(DateTime(DateTime.now().year, 1, 1))
        .inDays;
    
    final filteredQuotes = _quotes.where((q) {
      if (isFemale == null || q['isFemale'] == null) return true;
      return q['isFemale'] == isFemale.toString();
    }).toList();

    return filteredQuotes[dayOfYear % filteredQuotes.length];
  }

  static Map<String, String> getRandomQuote() {
    final index = DateTime.now().millisecondsSinceEpoch % _quotes.length;
    return _quotes[index];
  }

  static List<Map<String, String>> get allQuotes => _quotes;
